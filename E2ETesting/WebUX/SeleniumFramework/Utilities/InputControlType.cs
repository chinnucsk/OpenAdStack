namespace SeleniumFramework.Utilities
{
    /// <summary>
    /// This enum contains all input control types used on web forms
    /// </summary>
    public enum InputControlType
    {
        /// <summary>
        /// No Form Control
        /// </summary>
        None,

        /// <summary>
        /// Html Form
        /// </summary>
        Form,

        /// <summary>
        /// Html Textbox
        /// </summary>
        TextBox,

        /// <summary>
        /// Dropdown List
        /// </summary>
        DropDownList,

        /// <summary>
        /// Html Button
        /// </summary>
        Button,

        /// <summary>
        /// Html Checkbox
        /// </summary>
        CheckBox,

        /// <summary>
        /// Html Image Control
        /// </summary>
        Image,

        /// <summary>
        /// Div control
        /// </summary>
        Div
    }
}
